const buildPriyaChapterPhotos = (
  urls: string[],
  idPrefix: 'ph' | 'pw',
  heroIndexes: number[],
  heroCaptions: Record<number, string>
): Photo[] => {
  return urls.map((url, index) => {
    const oneBased = index + 1
    const isHero = heroIndexes.includes(oneBased)
    return {
      id: `${idPrefix}${oneBased}`,
      url,
      aspect: oneBased % 3 === 0 ? 'landscape' : 'portrait',
      role: oneBased === 1 ? 'establish' : isHero ? 'hero' : oneBased % 5 === 0 ? 'detail' : 'candid',
      caption: isHero ? heroCaptions[oneBased] : undefined,
      favorited: isHero,
      views: 70 + oneBased * 5,
    }
  })
}

